// Opérateurs arithmétiques
let a = 10;
let b = 5;

console.log(a + b); // Affiche 15 (addition)
console.log(a - b); // Affiche 5 (soustraction)
console.log(a * b); // Affiche 50 (multiplication)
console.log(a / b); // Affiche 2 (division)

// Opérateurs de comparaison
console.log(a == b); // Affiche false (égalité)
console.log(a != b); // Affiche true (inégalité)
console.log(a > b); // Affiche true (supérieur)
console.log(a < b); // Affiche false (inférieur)

// Opérateurs booléens
let c = true;
let d = false;

console.log(c && d); // Affiche false (ET logique)
console.log(c || d); // Affiche true (OU logique)
console.log(!c); // Affiche false (NOT logique)


