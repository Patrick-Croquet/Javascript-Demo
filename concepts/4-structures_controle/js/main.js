// Instruction conditionnelle (if/else)
let age = 18;

if (age >= 18) {
  console.log("Vous êtes majeur.");
} else {
  console.log("Vous êtes mineur.");
}

// Boucle for
for (let i = 0; i < 5; i++) {
  console.log("i =", i);
}

// Boucle while
let j = 0;
while (j < 10) {
  console.log("j =", j);
  j++;
}
